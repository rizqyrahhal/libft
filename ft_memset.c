#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*p;
	size_t			i;

	p = b;
	i = 0;
	while (len--)
	{
		p[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
